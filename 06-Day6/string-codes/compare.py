str1 = "apple"
str2 = "banana"
str3 = "apple"

# Equality and inequality
print(str1 == str3)   # Output: True
print(str1 != str2)   # Output: True

# Lexicographical comparison
print(str1 < str2)    # Output: True ("apple" comes before "banana")
print(str1 > str2)    # Output: False
print(str1 >= str3)   # Output: True
print(str1 <= str3)   # Output: True
