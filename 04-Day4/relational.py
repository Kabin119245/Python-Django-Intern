# Integers comparison
a = 99
b = 58

print("Integer comparison:")
print(a > b)   # True
print(a < b)   # False
print(a == b)  # False
print(a != b)  # True
print(a >= b)  # True
print(a <= b)  # False

# Float comparison
x = 10.5
y = 20.3

print("\nFloat comparison:")
print(x > y)   # False
print(x < y)   # True
print(x == y)  # False
print(x != y)  # True
print(x >= y)  # False
print(x <= y)  # True

# String comparison
str1 = "apple"
str2 = "banana"

print("\nString comparison:")
print(str1 > str2)   # False (because 'a' comes before 'b' lexicographically)
print(str1 < str2)   # True
print(str1 == str2)  # False
print(str1 != str2)  # True
print(str1 >= str2)  # False
print(str1 <= str2)  # True

# Boolean comparison
bool1 = True
bool2 = False

print("\nBoolean comparison:")
print(bool1 > bool2)   # True (True is considered as 1 and False as 0)
print(bool1 < bool2)   # False
print(bool1 == bool2)  # False
print(bool1 != bool2)  # True
print(bool1 >= bool2)  # True
print(bool1 <= bool2)  # False
